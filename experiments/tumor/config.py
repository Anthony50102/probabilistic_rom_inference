# config.py
"""Configuration for TNBC Tumor Growth experiments using TumorTwin data.

Instead of implementing our own PDE solver, we use pre-computed full-order
model (FOM) data generated by TumorTwin's 3D reaction-diffusion solver
(Kapteyn et al. 2025). Data is stored as NPZ files in the data/ directory.

The reaction-diffusion PDE solved by TumorTwin is:
    du/dt = d ∇²u + k u (1 - u/θ)

The logistic growth term produces a quadratic ROM operator structure:
    dq̂/dt = ĉ + Âq̂ + Ĥ[q̂ ⊗ q̂]

No treatment (chemotherapy/radiotherapy) is applied — we simulate pure
tumor growth to avoid derivative discontinuities that break the GP
smoothness assumption.

Usage:
    1. First run: python generate_fom_data.py
    2. Then run: python 04_conditional_integral.py
"""

