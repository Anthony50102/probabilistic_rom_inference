# =============================================================================
# TumorTwin FOM data wrapper
# =============================================================================
class TumorTwinFOM:
    """Wrapper around pre-computed TumorTwin snapshot data.

    Provides the same interface as the PDE model classes in core/pde_models.py
    (noise, solve via interpolation) plus 3D reconstruction for visualization.
    """

    def __init__(self, data_path=None):
        if data_path is None:
            data_path = FOM_DATA_PATH
        if not os.path.exists(data_path):
            raise FileNotFoundError(
                f"FOM data not found: {data_path}\n"
                "Run `python generate_fom_data.py` first."
            )
        data = np.load(data_path, allow_pickle=True)
        self._snapshots = data['snapshots']       # (n_dof, n_time) float32
        self._times = data['times_days']           # (n_time,) float64
        self.grid_shape = tuple(data['grid_shape'])
        self.breast_mask = data['breast_mask']     # (nx, ny, nz) bool
        self.tumor_mask_t0 = data['tumor_mask_t0'] # (nx, ny, nz) bool
        self.spacing = data['spacing']             # (3,) mm
        self.patient_id = str(data['patient_id'])
        self.k = float(data['k'])
        self.d = float(data['d'])
        self.theta = float(data['theta'])

        # Precompute tumor centroid for slice visualization
        tumor_vox = np.argwhere(self.tumor_mask_t0)
        if len(tumor_vox) > 0:
            self._tumor_center = tumor_vox.mean(axis=0).astype(int)
        else:
            self._tumor_center = np.array(self.grid_shape) // 2

    @property
    def n_dof(self):
        return self._snapshots.shape[0]

    @property
    def t_max(self):
        return self._times[-1]

    def get_states(self, time_points):
        """Interpolate FOM snapshots to arbitrary time points.

        Parameters
        ----------
        time_points : ndarray of shape (k,)
            Times in days at which to evaluate the solution.

        Returns
        -------
        ndarray of shape (n_dof, k)
        """
        from scipy.interpolate import interp1d
        interp = interp1d(
            self._times, self._snapshots, axis=1,
            kind='cubic', fill_value='extrapolate',
        )
        return interp(time_points).astype(np.float64)

    @staticmethod
    def noise(states, noise_level=0):
        """Add Gaussian noise to snapshot data.

        Preserves the initial condition (first column).  Noise is added
        only to DOFs in the active tumor region (signal > 0.1% of max)
        to avoid clipping-bias artifacts in the POD projection.
        """
        if not noise_level:
            return states
        scale = noise_level * (states.max() - states.min())
        noised = states.copy()
        noise = np.random.normal(0, scale, noised[:, 1:].shape)
        # Mask: only add noise where there is actual signal
        threshold = 0.001 * states.max()
        mask = states[:, 1:] > threshold
        noised[:, 1:] += noise * mask
        return noised

    def reconstruct_3d(self, flat_state):
        """Reshape flat (n_dof,) vector to 3D (nx, ny, nz) volume."""
        return flat_state.reshape(self.grid_shape)

    def get_tumor_center(self):
        """Return (ix, iy, iz) voxel indices of tumor centroid."""
        return tuple(self._tumor_center)

    def get_center_slices(self, flat_state):
        """Get axial/coronal/sagittal slices through tumor centroid.

        Returns dict with keys 'axial', 'coronal', 'sagittal', 'center'.
        """
        vol = self.reconstruct_3d(flat_state)
        cx, cy, cz = self._tumor_center
        return {
            'axial': vol[:, :, cz],         # XY plane at z=cz
            'coronal': vol[:, cy, :],        # XZ plane at y=cy
            'sagittal': vol[cx, :, :],       # YZ plane at x=cx
            'center': (cx, cy, cz),
        }


def load_fom_data(
    prediction_time_domain,
    training_span,
    num_samples,
    noise_level=0.0,
    data_path=None,
):
    """Load TumorTwin data, matching generate_trajectory() interface.

    Parameters
    ----------
    prediction_time_domain : ndarray
        Full time grid for truth solution.
    training_span : (float, float)
        (t_start, t_end) for training data.
    num_samples : int
        Number of training snapshots to subsample.
    noise_level : float
        Gaussian noise level (fraction of state range).
    data_path : str, optional
        Path to FOM NPZ file.

    Returns
    -------
    fom : TumorTwinFOM
    t_full : ndarray of shape (n_pred,)
    true_states : ndarray of shape (n_dof, n_pred)
    t_sampled : ndarray of shape (num_samples,)
    snaps_sampled : ndarray of shape (n_dof, num_samples)
    """
    fom = TumorTwinFOM(data_path)

    # Full truth solution over prediction domain
    true_states = fom.get_states(prediction_time_domain)

    # Uniform random subsample from training span
    t_sampled = np.sort(
        np.random.uniform(training_span[0], training_span[1], size=num_samples)
    )
    t_sampled[0] = training_span[0]
    t_sampled[-1] = training_span[1]

    # Noisy training snapshots
    snaps_sampled = fom.noise(fom.get_states(t_sampled), noise_level)

    return fom, prediction_time_domain, true_states, t_sampled, snaps_sampled


